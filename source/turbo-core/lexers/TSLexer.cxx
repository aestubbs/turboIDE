// An injection naming a language we have not vendored is skipped, and the region
// simply keeps its host's styling. That is deliberate and load-bearing: the
// grammars' own queries ask for regex, sql, markdown, python, jsdoc, vue and
// more, and we are never going to carry all of them.
const Grammar *grammarNamed(const std::string &name)
{
    for (const NamedGrammar &n : kGrammars)
        if (name == n.name) {
            const Grammar &g = n.get();
            return g.ok ? &g : nullptr;
        }
    return nullptr;
}

std::string_view nodeText(const std::string &text, TSNode n)
{
    const uint32_t s = ts_node_start_byte(n);
    const uint32_t e = ts_node_end_byte(n);
    if (s >= e || e > text.size())
        return {};
    return std::string_view(text).substr(s, e - s);
}

bool predicatesPass(const std::vector<Predicate> &preds, const TSQueryMatch &m,
                    const std::string &text)
{
    auto capturedText = [&](uint32_t captureId) -> std::string_view {
        for (uint16_t i = 0; i < m.capture_count; ++i)
            if (m.captures[i].index == captureId)
                return nodeText(text, m.captures[i].node);
        return {};
    };

    for (const Predicate &p : preds) {
        if (p.op == "set!" || p.args.empty() || !p.args[0].isCapture)
            continue;                                  // directives and unknowns pass
        const std::string_view lhs = capturedText(p.args[0].capture);

        if (p.op == "eq?" || p.op == "not-eq?") {
            if (p.args.size() < 2) continue;
            const std::string_view rhs = p.args[1].isCapture
                ? capturedText(p.args[1].capture)
                : std::string_view(p.args[1].str);
            const bool eq = (lhs == rhs);
            if (eq != (p.op == "eq?"))
                return false;
        } else if (p.op == "match?" || p.op == "not-match?") {
            if (!p.hasRe) continue;
            // regex_search, not regex_match: the Elixir query uses "^_" to catch
            // an unused variable, and regex_match anchors both ends, so it would
            // only fire on a variable literally named "_".
            const std::string s(lhs);
            const bool hit = std::regex_search(s, p.re);
            if (hit != (p.op == "match?"))
                return false;
        } else if (p.op == "any-of?" || p.op == "not-any-of?") {
            bool hit = false;
            for (size_t i = 1; i < p.args.size(); ++i)
                if (!p.args[i].isCapture && lhs == p.args[i].str) {
                    hit = true;
                    break;
                }
            if (hit != (p.op == "any-of?"))
                return false;
        }
        // Anything else (#is?, #offset!, ...) is treated as passing, which is
        // what tree-sitter-highlight does with its general_predicates.
    }
    return true;
}

