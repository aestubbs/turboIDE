// Query predicates.
//
// The C API stores predicates but does not evaluate them -- that lives in the
// Rust binding. We must evaluate the ones these grammars actually use:
// #eq?, #match?, #any-of? (and #set!, which carries the injection directives).

struct PredArg {
    bool isCapture = false;
    uint32_t capture = 0;
    std::string str;
};

struct Predicate {
    std::string op;
    std::vector<PredArg> args;
    std::regex re;
    bool hasRe = false;
};

// `(#offset! @cap sr sc er ec)` shifts the captured range's start and end points
// by the given row/column deltas. HEEx uses it to peel a <script>...</script>
// tag down to its contents (it captures the whole tag, then trims 1 row off the
// front and 9 columns off the back). Ignoring it -- as tree-sitter-highlight
// does -- means handing the JavaScript parser the surrounding markup, which is a
// syntax error, so the script block simply does not highlight.
struct InjOffset {
    int startRow = 0, startCol = 0, endRow = 0, endCol = 0;
    bool set = false;
};

struct InjSettings {
    std::string language;
    bool combined = false;
    bool includeChildren = false;
    InjOffset offset;
};

std::string queryStr(const TSQuery *q, uint32_t id)
{
    uint32_t len = 0;
    const char *s = ts_query_string_value_for_id(q, id, &len);
    return std::string(s, len);
}

std::string captureName(const TSQuery *q, uint32_t id)
{
    uint32_t len = 0;
    const char *s = ts_query_capture_name_for_id(q, id, &len);
    return std::string(s, len);
}

// Predicate steps are a flat array terminated by Done sentinels -- one Done per
// predicate. They must be split on that sentinel, NOT read in fixed groups of
// three: #any-of? is variadic and "#set! injection.combined" is only two steps.
std::vector<std::vector<Predicate>> loadPredicates(const TSQuery *q)
{
    std::vector<std::vector<Predicate>> byPattern(ts_query_pattern_count(q));
    for (uint32_t p = 0; p < byPattern.size(); ++p) {
        uint32_t n = 0;
        const TSQueryPredicateStep *steps = ts_query_predicates_for_pattern(q, p, &n);
        Predicate cur;
        bool first = true;
        for (uint32_t i = 0; i < n; ++i) {
            const TSQueryPredicateStep &s = steps[i];
            if (s.type == TSQueryPredicateStepTypeDone) {
                if (!cur.op.empty()) {
                    if ((cur.op == "match?" || cur.op == "not-match?") &&
                        cur.args.size() >= 2 && !cur.args[1].isCapture) {
                        try {
                            // ECMAScript is fine for every regex these grammars
                            // actually use (^_, ^[sS]$, ^[rR]$, ^on[a-z]+$), and
                            // compiling here keeps std::regex off the hot path.
                            cur.re.assign(cur.args[1].str, std::regex::ECMAScript);
                            cur.hasRe = true;
                        } catch (const std::regex_error &) {
                            cur.hasRe = false;
                        }
                    }
                    byPattern[p].push_back(std::move(cur));
                }
                cur = Predicate();
                first = true;
                continue;
            }
            if (first) {
                cur.op = queryStr(q, s.value_id);   // the operator is always a string
                first = false;
                continue;
            }
            PredArg a;
            if (s.type == TSQueryPredicateStepTypeCapture) {
                a.isCapture = true;
                a.capture = s.value_id;
            } else {
                a.str = queryStr(q, s.value_id);
            }
            cur.args.push_back(std::move(a));
        }
    }
    return byPattern;
}

// #set! directives are returned in the same step array as the real predicates,
// so the injection settings are read straight out of them.
std::vector<InjSettings> loadInjSettings(const std::vector<std::vector<Predicate>> &preds)
{
    std::vector<InjSettings> out(preds.size());
    for (size_t p = 0; p < preds.size(); ++p)
        for (const Predicate &pr : preds[p]) {
            if (pr.op != "set!" || pr.args.empty() || pr.args[0].isCapture)
                continue;
            const std::string &key = pr.args[0].str;
            if (key == "injection.language" && pr.args.size() >= 2)
                out[p].language = pr.args[1].str;
            else if (key == "injection.combined")
                out[p].combined = true;
            else if (key == "injection.include-children")
                out[p].includeChildren = true;
        }
    // #offset! is a predicate, not a #set!, so it arrives with the capture as its
    // first argument and the four deltas after it.
    for (size_t p = 0; p < preds.size(); ++p)
        for (const Predicate &pr : preds[p]) {
            if (pr.op != "offset!" || pr.args.size() < 5)
                continue;
            out[p].offset.startRow = atoi(pr.args[1].str.c_str());
            out[p].offset.startCol = atoi(pr.args[2].str.c_str());
            out[p].offset.endRow = atoi(pr.args[3].str.c_str());
            out[p].offset.endCol = atoi(pr.args[4].str.c_str());
            out[p].offset.set = true;
        }
    return out;
}

struct Grammar {
    const TSLanguage *language = nullptr;
    TSQuery *highlights = nullptr;
    TSQuery *injections = nullptr;
    std::vector<int> captureStyle;                  // highlight capture id -> style
    std::vector<uint32_t> injContentCaptures;       // injection capture ids named injection.content
    std::vector<uint32_t> injLangCaptures;          // ... named injection.language (dynamic form)
    std::vector<std::vector<Predicate>> hlPreds;
    std::vector<std::vector<Predicate>> injPreds;
    std::vector<InjSettings> injSettings;
    bool ok = false;
};

int styleForCapture(const std::string &name, const CaptureMap *map, size_t n)
{
    // Dotted-prefix fallback: string.special.symbol -> string.special -> string.
    std::string probe = name;
    for (;;) {
        for (size_t i = 0; i < n; ++i)
            if (probe == map[i].name)
                return map[i].style;
        auto dot = probe.rfind('.');
        if (dot == std::string::npos)
            return kNoStyle;
        probe.resize(dot);
    }
}

TSQuery *compileQuery(const TSLanguage *lang, const std::string &src)
{
    uint32_t errOffset = 0;
    TSQueryError errType = TSQueryErrorNone;
    return ts_query_new(lang, src.c_str(), (uint32_t) src.size(), &errOffset, &errType);
}

// Queries come in pieces and must be concatenated, for three separate reasons:
//   - a grammar can split them (JavaScript keeps JSX in highlights-jsx.scm, PHP
//     keeps its HTML injection in injections-text.scm);
//   - a dialect can be a delta on another (TypeScript's highlights are only the
//     type-syntax additions to JavaScript's);
//   - a query can open with `; inherits: html`, which is an nvim-treesitter
//     convention rather than anything tree-sitter core understands. Prepending
//     the inherited grammar's source here *is* the implementation of it.
std::string concatQueries(std::initializer_list<const char *> srcs)
{
    std::string out;
    for (const char *s : srcs)
        if (s && *s) {
            out += s;
            out += '\n';
        }
    return out;
}

Grammar buildGrammar(const TSLanguage *lang,
                     std::initializer_list<const char *> hlSrcs,
                     std::initializer_list<const char *> injSrcs,
                     const CaptureMap *map, size_t nMap)
{
    Grammar g;
    g.language = lang;
    g.highlights = compileQuery(lang, concatQueries(hlSrcs));
    g.injections = compileQuery(lang, concatQueries(injSrcs));
    if (!g.highlights || !g.injections)
        return g;

    const uint32_t nHl = ts_query_capture_count(g.highlights);
    g.captureStyle.resize(nHl, kNoStyle);
    for (uint32_t i = 0; i < nHl; ++i)
        g.captureStyle[i] = styleForCapture(captureName(g.highlights, i), map, nMap);

    const uint32_t nInj = ts_query_capture_count(g.injections);
    for (uint32_t i = 0; i < nInj; ++i) {
        const std::string n = captureName(g.injections, i);
        if (n == "injection.content")
            g.injContentCaptures.push_back(i);
        else if (n == "injection.language")
            g.injLangCaptures.push_back(i);
    }

    g.hlPreds = loadPredicates(g.highlights);
    g.injPreds = loadPredicates(g.injections);
    g.injSettings = loadInjSettings(g.injPreds);
    g.ok = true;
    return g;
}

