void tsLog(const char *fmt, ...)
{
    static FILE *f = [] () -> FILE * {
        const char *path = getenv("TURBO_TS_LOG");
        return (path && path[0]) ? fopen(path, "w") : nullptr;
    }();
    if (!f)
        return;
    va_list ap;
    va_start(ap, fmt);
    vfprintf(f, fmt, ap);
    va_end(ap);
    fflush(f);
}

