// A Scintilla lexer backed by tree-sitter, for Elixir and HEEx.
//
// Why a parser rather than a state machine: HEEx is a three-level embedding --
// Elixir -> ~H"""...""" -> HTML -> {...} / <%= %> -> Elixir -- and the `{` that
// opens an expression is only an expression opener depending on the surrounding
// HTML tag state. Regex/state-machine lexers get this wrong in ways their
// authors have not been able to fix; VS Code's HEEx grammar has been visibly
// broken on `mix format` output since 2021. A parser makes the nesting fall out
// for free.
//
// Grammars and queries live in the submodules (deps/tree-sitter-*), Apache-2.0
// (Elixir) and MIT (HEEx and the tree-sitter runtime). See THIRD-PARTY-NOTICES.
//
// Structure: parse the document with the root grammar, run its highlight query,
// then run its injection query to find embedded regions and recurse into them
// with ts_parser_set_included_ranges. Every capture from every layer is
// collected as a (range, depth, pattern) triple, then sorted and painted over a
// flat style buffer -- the sort is what implements tree-sitter's precedence
// rules, and getting it right is most of the subtlety here.

